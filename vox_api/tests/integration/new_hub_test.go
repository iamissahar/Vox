func TestNewHubHandler_ConflictDoesNotCreateAdditionalHub(t *testing.T) {
	mgr := hub.NewManager()
	existingID := mgr.New() // one hub in the manager

	api := &hub.HubAPI{MGR: mgr}
	r := helpers.NewHubRouter(t, api)

	w := httptest.NewRecorder()
	r.ServeHTTP(w, helpers.NewHubRequest(existingID))

	require.Equal(t, http.StatusConflict, w.Code)

	// Manager must still contain exactly one hub — the original.
	_, ok := mgr.Get(existingID)
	assert.True(t, ok, "original hub must still exist after conflict")
}
