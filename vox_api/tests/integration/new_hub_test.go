func TestNewHubHandler_HubAlreadyExists(t *testing.T) {
	mgr := hub.NewManager()
	existingID := mgr.New() // seed a real hub

	api := &hub.HubAPI{MGR: mgr}
	r := helpers.NewHubRouter(t, api)

	w := httptest.NewRecorder()
	r.ServeHTTP(w, helpers.NewHubRequest(existingID))

	assert.Equal(t, http.StatusConflict, w.Code)
}

